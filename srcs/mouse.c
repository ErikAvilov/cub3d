#include "functions.h"

int	mouse_move(int x, int y, t_mlx_data *mlx_data)
{
	if ((x > 1260 || x < 20) || (x < 740 && x > 540) || (y > 700 || y < 20))
		mlx_data->mouse.x = -1;
	else
		mlx_data->mouse.x = x;
	return (0);
}
